package ARRAYS;

public class Basics2 {
    public static void main(String[] args) {
        int[] arr; // Array named arr is initialized
        arr = new int[7]; // Array named arr of size 7 is Declared
        System.out.println(arr[0]); // Can print an element using indexing
        // arr[0] will be storing 0 as default value which will be updated later
        arr[0] = 88; // Array value can be updated
        System.out.println(arr[0]);


    }
}
