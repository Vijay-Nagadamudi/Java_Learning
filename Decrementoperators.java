package BASICS;

public class Decrementoperators {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x--); // First Used and then decremented.
        System.out.println(x);
        int y = 10;
        System.out.println(--y); // First decremented and then Used.
        System.out.println(y);
    }
}
