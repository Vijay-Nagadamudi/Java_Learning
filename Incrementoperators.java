package BASICS;


public class Incrementoperators {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x++); // First the x is used and then the x is incremented.
        System.out.println(x);
        int y = 10;
        System.out.println(++y);  //First the y is incremented and then it is used.
        System.out.println(y);
    }
}

