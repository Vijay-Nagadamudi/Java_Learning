package BASICS;

public class PrintingNumbers {
     public static void main(String args[]){
         System.out.println("4"); // We get 4 as output
         System.out.println("4+3");// We get 4+3 as output
         System.out.println(4);//We get 4 as output
         System.out.println(4+3);//We get 7 as output
     }
}

// When we are putting anything on double quotes system takes it as a String input.
